ALTER PROCEDURE [dbo].[PROC_SAVE_PLAYER_VERSION_TREE_OPERATIONALCHART] 
	@SOURCE_VERSION AS VARCHAR(50),
	@DESTINATION_VERSION AS VARCHAR(50),
	@POSITION_ID AS VARCHAR(50),
	@COMPANYNAME AS VARCHAR(500),
	@USERID AS VARCHAR(50),
	@PARENT_POSITION_ID AS VARCHAR(50),
	@CHILD_FIELD AS VARCHAR(50),
	@PARENT_FIELD AS VARCHAR(50) 
AS
BEGIN
	-- SET NOCOUNT ON added to prevent extra result sets from
	-- interfering with SELECT statements.
	SET NOCOUNT ON;

	DECLARE @SQLQUERY AS NVARCHAR(MAX);
	DECLARE @CHILD_VALUE AS VARCHAR(MAX);
	DECLARE @COMPANYTB AS VARCHAR(500);
	SET @COMPANYTB = @COMPANYNAME + '_LEVELINFOS';


	SET @SQLQUERY = 'SELECT TOP 1 @CHILD_VALUE=['+@CHILD_FIELD+'] FROM ['+@COMPANYTB+'] WHERE LEVEL_ID='''+@PARENT_POSITION_ID+''';';
    EXEC sp_executesql @SQLQUERY, N'@CHILD_VALUE NVARCHAR(1024) OUTPUT', @CHILD_VALUE OUTPUT;

	SET @SQLQUERY = 'DROP TABLE IF EXISTS ['+@USERID+'_LEVELINFOS];
	                 SELECT * INTO '+@USERID+'_LEVELINFOS FROM ['+@COMPANYTB+'] WHERE VERSION='''+@SOURCE_VERSION+''' AND BREAD_GRAM like ''%'+@POSITION_ID+'%'';
					 UPDATE ['+@USERID+'_LEVELINFOS] SET USER_ID='''+@USERID+''', VERSION='''+@DESTINATION_VERSION+''', VERIFY_FLAG=''N'';
					 UPDATE ['+@USERID+'_LEVELINFOS] SET PARENT_LEVEL_ID='''+@PARENT_POSITION_ID+''', '+@PARENT_FIELD+'='''+@CHILD_VALUE+''' WHERE LEVEL_ID='''+@POSITION_ID+''';
					 DELETE FROM ['+@COMPANYTB+'] WHERE VERSION='''+@DESTINATION_VERSION+''' AND LEVEL_ID IN (SELECT LEVEL_ID FROM ['+@USERID+'_LEVELINFOS]);
					 INSERT INTO ['+@COMPANYTB+'] SELECT * FROM ['+@USERID+'_LEVELINFOS];'

	EXEC(@SQLQUERY);

	EXEC PROC_GET_TREE_OPERATIONALCHART_NOR_SOC 'Player', @DESTINATION_VERSION, @COMPANYNAME, @USERID 
END


